/**
 * Specifies a picture alignment when drawing pictures.
 */
export enum PictureAlignment {
  /**
   * Left-top alignment.
   */
  LeftTop,
  /**
   * Left-center alignment.
   */
  LeftCenter,
  /**
   * Left-bottom alignment.
   */
  LeftBottom,
  /**
   * Center-top alignment.
   */
  CenterTop,
  /**
   * Center-center alignment.
   */
  CenterCenter,
  /**
   * Center-bottom alignment.
   */
  CenterBottom,
  /**
   * Right-top alignment.
   */
  RightTop,
  /**
   * Right-center alignment.
   */
  RightCenter,
  /**
   * Right-bottom alignment.
   */
  RightBottom,
}
